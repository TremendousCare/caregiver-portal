-- Follow-up tasks v1 — concrete, scheduled, completable work items
-- generated automatically when a caregiver-client match first appears
-- in the schedule.
--
-- Office-coordinator feedback (Juliana, 2026-05-22), items #8 + #10:
--   "Create a task dashboard with automated follow-up reminders for
--    both clients and caregivers, including: First day check-in,
--    First week follow-up, Second week follow-up, Monthly check-ins."
--   "Add an option for the system to automatically generate follow-up
--    tasks whenever a caregiver is matched with a new client."
--
-- Design (locked with the owner 2026-05-23):
--
--   1. Two-layer model:
--      • follow_up_templates — configurable cadences (org-scoped).
--        Seeded with 4 defaults: 0d / 7d / 14d / 30d-monthly.
--      • follow_up_tasks — concrete instances generated by triggers.
--
--   2. Anchor: "day of the client's first scheduled shift with this
--      caregiver." NOT "first completed shift" — long shifts ending
--      at 8pm would miss the same-day reminder window. Due-time is
--      09:00 local on the calculated date so the task lands on the
--      Tasks dashboard at the start of business hours.
--
--   3. Triggers:
--      • shifts (AFTER INSERT/UPDATE): if (cg, client) first appears
--        in the schedule, generate instances anchored to NEW.start_time.
--      • caregiver_assignments (AFTER UPDATE): if status flips to
--        'ended', cancel any pending follow-ups for that match.
--
--   4. Recurrence: a cron job (added in a follow-up migration with
--      the edge function) auto-spawns the next instance of a recurring
--      template when the prior one is completed. v1 ships the schema;
--      cron wiring lands when the dispatcher edge function does.
--
--   5. Backfill: NONE. Only new matches going forward get follow-ups.
--      Avoids dropping a backlog of overdue tasks on launch day.
--
-- Multi-tenancy (CLAUDE.md → Prime Directives):
--   • Both tables NOT NULL org_id DEFAULT public.default_org_id().
--   • RLS staff-only (admin + member), org-scoped, via public.is_staff().
--   • No caregiver access in v1 — the PWA does not surface tasks.
--
-- Idempotent: every CREATE / DROP POLICY / DROP TRIGGER uses
-- IF [NOT] EXISTS so the Deploy Database Migrations workflow can
-- re-run safely.

-- ────────────────────────────────────────────────────────────────────
-- 1. follow_up_templates
-- ────────────────────────────────────────────────────────────────────
-- Configurable cadences. Slug + org_id is UNIQUE so the seed and any
-- subsequent edits-by-slug stay coherent across re-runs.
--
-- anchor_event is a CHECK enum so v2 can add new sources (e.g.
-- 'assignment_started', 'phase_change') without code surgery.
--
-- recurring_interval_days NULL means "one-shot". Non-null means the
-- next instance is auto-spawned that many days after the prior is
-- marked done (handled by the cron in a follow-up migration).

CREATE TABLE IF NOT EXISTS public.follow_up_templates (
  id                       uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  org_id                   uuid NOT NULL DEFAULT public.default_org_id()
                             REFERENCES public.organizations(id) ON DELETE CASCADE,
  slug                     text NOT NULL,
  name                     text NOT NULL,
  description              text,
  guidance                 text,
  anchor_event             text NOT NULL DEFAULT 'first_scheduled_shift_day'
                             CHECK (anchor_event IN (
                               'first_scheduled_shift_day',
                               'assignment_started'
                             )),
  offset_days              integer NOT NULL DEFAULT 0
                             CHECK (offset_days >= 0),
  recurring_interval_days  integer
                             CHECK (recurring_interval_days IS NULL
                                    OR recurring_interval_days > 0),
  target_type              text NOT NULL DEFAULT 'both'
                             CHECK (target_type IN ('caregiver', 'client', 'both')),
  default_urgency          text NOT NULL DEFAULT 'warning'
                             CHECK (default_urgency IN ('critical', 'warning', 'info')),
  default_assignee_email   text,
  enabled                  boolean NOT NULL DEFAULT true,
  sort_order               integer NOT NULL DEFAULT 0,
  created_at               timestamptz NOT NULL DEFAULT now(),
  updated_at               timestamptz NOT NULL DEFAULT now(),
  UNIQUE (org_id, slug)
);

CREATE INDEX IF NOT EXISTS idx_follow_up_templates_org
  ON public.follow_up_templates (org_id);
CREATE INDEX IF NOT EXISTS idx_follow_up_templates_enabled
  ON public.follow_up_templates (org_id, enabled, anchor_event)
  WHERE enabled;

DROP TRIGGER IF EXISTS follow_up_templates_touch_updated_at
  ON public.follow_up_templates;
CREATE TRIGGER follow_up_templates_touch_updated_at
  BEFORE UPDATE ON public.follow_up_templates
  FOR EACH ROW EXECUTE FUNCTION public.touch_updated_at();

-- ────────────────────────────────────────────────────────────────────
-- 2. follow_up_tasks
-- ────────────────────────────────────────────────────────────────────
-- Concrete instances. UNIQUE (template_id, caregiver_id, client_id,
-- anchor_shift_id) is the idempotency key — if the trigger fires twice
-- for the same shift, the second insert is rejected cleanly via ON
-- CONFLICT DO NOTHING.
--
-- Status state machine:
--   pending → done (via mark-complete)
--   pending → snoozed (with snoozed_until) → pending (when snooze expires; not enforced at DB layer)
--   pending → cancelled (when assignment ends)
--
-- assigned_to / completed_by are text (matches existing convention —
-- e.g. caregivers.id is text, the events table actor is text).

CREATE TABLE IF NOT EXISTS public.follow_up_tasks (
  id                  uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  org_id              uuid NOT NULL DEFAULT public.default_org_id()
                        REFERENCES public.organizations(id) ON DELETE CASCADE,
  template_id         uuid NOT NULL REFERENCES public.follow_up_templates(id) ON DELETE CASCADE,
  caregiver_id        text NOT NULL REFERENCES public.caregivers(id) ON DELETE CASCADE,
  client_id           text NOT NULL REFERENCES public.clients(id) ON DELETE CASCADE,
  anchor_shift_id     uuid REFERENCES public.shifts(id) ON DELETE SET NULL,
  due_at              timestamptz NOT NULL,
  status              text NOT NULL DEFAULT 'pending'
                        CHECK (status IN ('pending', 'done', 'snoozed', 'cancelled')),
  urgency             text NOT NULL DEFAULT 'warning'
                        CHECK (urgency IN ('critical', 'warning', 'info')),
  assigned_to         text,
  snoozed_until       timestamptz,
  completed_at        timestamptz,
  completed_by        text,
  completion_note     text,
  cancellation_reason text,
  generated_at        timestamptz NOT NULL DEFAULT now(),
  created_at          timestamptz NOT NULL DEFAULT now(),
  updated_at          timestamptz NOT NULL DEFAULT now(),
  -- Idempotency key: never two of the same template for the same
  -- match anchored to the same shift. Recurring templates use
  -- anchor_shift_id = NULL for spawned instances, so the partial
  -- unique index below covers them separately.
  UNIQUE (template_id, caregiver_id, client_id, anchor_shift_id)
);

-- For recurring templates, anchor_shift_id is NULL on spawned
-- instances. UNIQUE (template_id, cg, client, NULL) treats NULL as
-- distinct so we still need a guard against duplicate pending
-- recurring instances. Partial unique index on the pending state.
CREATE UNIQUE INDEX IF NOT EXISTS uq_follow_up_tasks_pending_recurring
  ON public.follow_up_tasks (template_id, caregiver_id, client_id)
  WHERE anchor_shift_id IS NULL AND status = 'pending';

-- Hot path: "what's due today across all assignees" — Tasks dashboard.
CREATE INDEX IF NOT EXISTS idx_follow_up_tasks_due_at
  ON public.follow_up_tasks (org_id, due_at)
  WHERE status = 'pending';

-- "Show me follow-ups for this caregiver" / "for this client" — the
-- inline panels on detail pages.
CREATE INDEX IF NOT EXISTS idx_follow_up_tasks_caregiver
  ON public.follow_up_tasks (caregiver_id, status, due_at);
CREATE INDEX IF NOT EXISTS idx_follow_up_tasks_client
  ON public.follow_up_tasks (client_id, status, due_at);

CREATE INDEX IF NOT EXISTS idx_follow_up_tasks_org
  ON public.follow_up_tasks (org_id);

DROP TRIGGER IF EXISTS follow_up_tasks_touch_updated_at
  ON public.follow_up_tasks;
CREATE TRIGGER follow_up_tasks_touch_updated_at
  BEFORE UPDATE ON public.follow_up_tasks
  FOR EACH ROW EXECUTE FUNCTION public.touch_updated_at();

-- ────────────────────────────────────────────────────────────────────
-- 3. RLS — staff-only, org-scoped
-- ────────────────────────────────────────────────────────────────────
-- Tasks are office-staff territory. Caregivers do not see them in v1
-- (the PWA still uses CarePlanChecklist for client-care tasks).
-- Both templates and instances use the same is_staff() gate.

ALTER TABLE public.follow_up_templates ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.follow_up_tasks     ENABLE ROW LEVEL SECURITY;

DO $$
DECLARE
  t text;
  cmd text;
  full_check text;
BEGIN
  full_check := 'public.is_staff() AND org_id = nullif((auth.jwt() ->> ''org_id''), '''')::uuid';
  FOR t IN SELECT unnest(ARRAY['follow_up_templates', 'follow_up_tasks']) LOOP
    FOR cmd IN SELECT unnest(ARRAY['select', 'insert', 'update', 'delete']) LOOP
      EXECUTE format('DROP POLICY IF EXISTS %I ON public.%I',
                     t || '_staff_' || cmd, t);
    END LOOP;
  END LOOP;
END $$;

CREATE POLICY follow_up_templates_staff_select ON public.follow_up_templates
  FOR SELECT TO authenticated
  USING (public.is_staff() AND org_id = nullif((auth.jwt() ->> 'org_id'), '')::uuid);
CREATE POLICY follow_up_templates_staff_insert ON public.follow_up_templates
  FOR INSERT TO authenticated
  WITH CHECK (public.is_staff() AND org_id = nullif((auth.jwt() ->> 'org_id'), '')::uuid);
