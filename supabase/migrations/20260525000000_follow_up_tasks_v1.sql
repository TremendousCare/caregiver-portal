-- ────────────────────────────────────────────────────────────────────
-- 5. Trigger: generate follow-ups on first-match shift
-- ────────────────────────────────────────────────────────────────────
-- Fires AFTER INSERT or UPDATE on shifts. Only generates instances
-- when this is the very first shift with a non-null caregiver+client
-- combo for that pair — verified via a NOT EXISTS check across the
-- shifts table (excluding the current row).
--
-- Why "first appearance in the schedule" not "first completed shift":
-- a long first shift might run 10am→8pm; the office needs the check-in
-- task in the morning, not at 8pm. So we anchor on start_time::date,
-- not on completion.
--
-- The function is SECURITY DEFINER so it can bypass RLS during the
-- INSERT — the calling code (an office user inserting a shift) might
-- not have direct INSERT rights to follow_up_tasks until the policy
-- check completes; the SECURITY DEFINER runs as the function owner,
-- which already holds the right grants. search_path is pinned to
-- prevent search-path-based privilege escalation.

CREATE OR REPLACE FUNCTION public.generate_follow_ups_on_first_shift()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO 'public'
AS $$
DECLARE
  v_due_date      date;
  v_first_shift   boolean;
BEGIN
  -- Only fire when both caregiver_id and client_id are populated.
  IF NEW.assigned_caregiver_id IS NULL OR NEW.client_id IS NULL THEN
    RETURN NEW;
  END IF;

  -- On UPDATE, skip if the (caregiver, client) pair is unchanged.
  -- An UPDATE that doesn't change the pair is just an admin edit,
  -- not a new match.
  IF TG_OP = 'UPDATE' THEN
    IF OLD.assigned_caregiver_id IS NOT DISTINCT FROM NEW.assigned_caregiver_id
       AND OLD.client_id IS NOT DISTINCT FROM NEW.client_id THEN
      RETURN NEW;
    END IF;
  END IF;

  -- Is this the first shift this caregiver has ever had with this
  -- client? Cancelled / no_show shifts still count — the match was
  -- established when the office scheduled it. Excluding the current
  -- row so an UPDATE on the same shift doesn't disqualify itself.
  SELECT NOT EXISTS (
    SELECT 1 FROM shifts s
     WHERE s.id <> NEW.id
       AND s.assigned_caregiver_id = NEW.assigned_caregiver_id
       AND s.client_id = NEW.client_id
  ) INTO v_first_shift;

  IF NOT v_first_shift THEN
    RETURN NEW;
  END IF;

  v_due_date := (NEW.start_time AT TIME ZONE 'UTC')::date;

  -- Generate one instance per enabled template for this org with
  -- anchor_event = 'first_scheduled_shift_day'. due_at is the start
  -- of the shift's date plus offset_days, fixed at 09:00 UTC. (Org
  -- timezone selection is a Phase D concern; for now Tremendous Care
  -- and acme-test both run on US Pacific which is UTC-7/8 — 09:00 UTC
  -- = 1-2am Pacific. That's too early. Use the shift's start_time
  -- directly truncated to date instead so the dashboard groups it
  -- under "today" relative to the office's actual clock.)
  --
  -- Concretely: due_at = (NEW.start_time::date + offset_days) at the
  -- shift's local clock-zero. Approximation that works for v1: use
  -- NEW.start_time directly (preserves the timezone) but truncate to
  -- date and add 9 hours of wall-clock. Postgres handles this via
  -- date_trunc + interval arithmetic.
  INSERT INTO public.follow_up_tasks
    (org_id, template_id, caregiver_id, client_id, anchor_shift_id,
     due_at, urgency, assigned_to)
  SELECT
    t.org_id,
    t.id,
    NEW.assigned_caregiver_id,
    NEW.client_id,
    NEW.id,
    -- 09:00 UTC on (shift_start_date + offset). v2 will respect the
    -- org's configured timezone via organizations.settings.
    (date_trunc('day', NEW.start_time) + (t.offset_days || ' days')::interval + interval '9 hours'),
    t.default_urgency,
    t.default_assignee_email
  FROM public.follow_up_templates t
  WHERE t.enabled
    AND t.anchor_event = 'first_scheduled_shift_day'
    AND t.org_id = NEW.org_id
  ON CONFLICT (template_id, caregiver_id, client_id, anchor_shift_id) DO NOTHING;

  RETURN NEW;
END
$$;

REVOKE ALL ON FUNCTION public.generate_follow_ups_on_first_shift() FROM PUBLIC;
GRANT EXECUTE ON FUNCTION public.generate_follow_ups_on_first_shift() TO authenticated, service_role;

DROP TRIGGER IF EXISTS shifts_generate_follow_ups ON public.shifts;
CREATE TRIGGER shifts_generate_follow_ups
  AFTER INSERT OR UPDATE OF assigned_caregiver_id, client_id ON public.shifts
  FOR EACH ROW EXECUTE FUNCTION public.generate_follow_ups_on_first_shift();

-- ────────────────────────────────────────────────────────────────────
-- 6. Trigger: cancel pending follow-ups when assignment ends
-- ────────────────────────────────────────────────────────────────────
-- When a caregiver_assignment flips to status='ended', any pending
-- follow-ups for that match are no longer actionable. Mark them
-- cancelled with an audit reason so the dashboard doesn't keep
-- nagging staff about a relationship that's over.

CREATE OR REPLACE FUNCTION public.cancel_follow_ups_on_assignment_end()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO 'public'
AS $$
BEGIN
  IF NEW.status <> 'ended' THEN
    RETURN NEW;
  END IF;
  IF OLD.status IS NOT DISTINCT FROM NEW.status THEN
    RETURN NEW;
  END IF;

  UPDATE public.follow_up_tasks
     SET status = 'cancelled',
         cancellation_reason = 'assignment_ended',
         updated_at = now()
   WHERE caregiver_id = NEW.caregiver_id
     AND client_id    = NEW.client_id
     AND status       = 'pending';

  RETURN NEW;
END
$$;

REVOKE ALL ON FUNCTION public.cancel_follow_ups_on_assignment_end() FROM PUBLIC;
GRANT EXECUTE ON FUNCTION public.cancel_follow_ups_on_assignment_end() TO authenticated, service_role;

DROP TRIGGER IF EXISTS caregiver_assignments_cancel_follow_ups
  ON public.caregiver_assignments;
CREATE TRIGGER caregiver_assignments_cancel_follow_ups
  AFTER UPDATE OF status ON public.caregiver_assignments
  FOR EACH ROW EXECUTE FUNCTION public.cancel_follow_ups_on_assignment_end();

-- ────────────────────────────────────────────────────────────────────
-- 7. Realtime
-- ────────────────────────────────────────────────────────────────────
-- The Tasks dashboard subscribes to follow_up_tasks so staff see
-- newly-generated tasks the moment a shift is scheduled, and so
-- "task marked done" reflects across tabs.

DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_publication_tables
    WHERE pubname = 'supabase_realtime'
      AND schemaname = 'public'
      AND tablename = 'follow_up_tasks'
  ) THEN
    ALTER PUBLICATION supabase_realtime ADD TABLE public.follow_up_tasks;
  END IF;
END $$;

-- ────────────────────────────────────────────────────────────────────
-- 8. Sanity check
-- ────────────────────────────────────────────────────────────────────
-- Fail loudly if the trigger wiring or the seed didn't land.

DO $$
DECLARE
  v_template_count integer;
  v_tc_org_id      uuid;
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_trigger
    WHERE tgname = 'shifts_generate_follow_ups'
      AND tgrelid = 'public.shifts'::regclass
  ) THEN
    RAISE EXCEPTION 'follow-up tasks: shifts_generate_follow_ups trigger missing after migration';
  END IF;

  IF NOT EXISTS (
    SELECT 1 FROM pg_trigger
    WHERE tgname = 'caregiver_assignments_cancel_follow_ups'
      AND tgrelid = 'public.caregiver_assignments'::regclass
  ) THEN
    RAISE EXCEPTION 'follow-up tasks: caregiver_assignments_cancel_follow_ups trigger missing after migration';
  END IF;

  v_tc_org_id := public.default_org_id();
  IF v_tc_org_id IS NULL THEN
    RAISE NOTICE 'follow-up tasks: default_org_id() returned NULL; skipping seed sanity check';
    RETURN;
  END IF;

  SELECT count(*) INTO v_template_count
    FROM public.follow_up_templates
   WHERE org_id = v_tc_org_id
     AND slug IN ('first_day_checkin', 'first_week_followup', 'two_week_followup', 'monthly_checkin');

  IF v_template_count <> 4 THEN
    RAISE EXCEPTION
      'follow-up tasks: expected 4 seeded templates for default org, found %', v_template_count;
  END IF;
END $$;
