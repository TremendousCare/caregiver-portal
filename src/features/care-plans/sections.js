// ═══════════════════════════════════════════════════════════════
// Care Plan — Section Metadata
//
// Canonical definition of the clinical care plan's structure. Every
// piece of UI (read-only panel, editor drawers, iPad intake wizard,
// caregiver app, AI context layer) reads section structure from this
// file — changing a label or order here propagates everywhere.
//
// Section shape:
//   {
//     id:               string, machine id (matches keys in version.data)
//     label:            string, human-readable section title
//     description:      string, one-line hint shown under the title
//     order:            number, display order (stable, low-first)
//     tiers:            string[], which audiences see this section.
//                       Any subset of ['admin', 'caregiver', 'family'].
//     isAutoGenerated:  bool,   if true, filled by AI (no manual entry)
//     usesTasksTable:   bool,   if true, content lives in care_plan_tasks
//                               (rendered as a task list, not fields)
//     fields:           Field[], field definitions for the editor
//   }
//
// Field shape:
//   {
//     id:          string, machine id (used as key in section data)
//     label:       string, human-readable field label
//     type:        FIELD_TYPES.<TYPE>
//     required?:   bool
//     readOnly?:   bool                 (for auto-generated fields)
//     cms485?:     bool                 (flags CMS-485 required fields)
//     help?:       string               (inline hint text)
//     placeholder?: string
//     options?:    string[]             (for select / multiselect / prn)
//     subfields?:  Field[]              (for list type — shape per row)
//     conditional?: { field, in? | equals? | notEquals? }
//                                       (hide unless sibling matches)
//     multiline?:  bool                 (for textarea — controls row count)
//   }
//
// Data storage:
//   `care_plan_versions.data` JSONB is shaped as
//     { [sectionId]: { [fieldId]: value, ... } }
//   Sections that aren't populated yet are simply missing from `data`.
//
// Visibility tiers:
//   - 'admin'     — office staff, full access (PR #164 RLS)
//   - 'caregiver' — in-home aide PWA (Phase 2d adds RLS + UI)
//   - 'family'    — via AI-generated digests in Communication Hub (Phase 3)
//
// CMS-485 compliance:
//   Fields marked `cms485: true` will be hydrated into CMS form 485
//   by a future export PR. No export logic yet — just the marking.
// ═══════════════════════════════════════════════════════════════


