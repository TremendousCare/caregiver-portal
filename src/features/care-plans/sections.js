/**
 * Look up a section definition by id. Returns undefined if the id
 * isn't known — callers should handle this (likely means stale data).
 */
export function getSectionById(id) {
  if (!id) return undefined;
  return CARE_PLAN_SECTIONS.find((s) => s.id === id);
}


/**
 * Sections sorted by display order.
 */
export function sortedSections() {
  return [...CARE_PLAN_SECTIONS].sort((a, b) => a.order - b.order);
}


/**
 * Does this section's content live in the care_plan_tasks table
 * rather than inline in version.data?
 */
export function sectionUsesTasks(section) {
  return Boolean(section?.usesTasksTable);
}


/**
 * Sections visible to a given audience tier. Used by the caregiver
 * PWA and the family digest AI to filter the plan.
 */
export function visibleSectionsForTier(tier) {
  if (!tier) return [];
  return sortedSections().filter((s) => Array.isArray(s.tiers) && s.tiers.includes(tier));
}


/**
 * Look up a field definition within a section. Returns undefined if
 * either the section or the field id is unknown. Used by change-event
 * logging to resolve human-readable field labels.
 */
export function getFieldById(sectionId, fieldId) {
  const section = getSectionById(sectionId);
  if (!section || !Array.isArray(section.fields)) return undefined;
  return section.fields.find((f) => f.id === fieldId);
}


