describe('Section fields', () => {
  it('every field has an id, label, and type', () => {
    for (const section of CARE_PLAN_SECTIONS) {
      for (const field of section.fields) {
        expect(field.id, `${section.id}.${field.id} id`).toBeTruthy();
        expect(field.label, `${section.id}.${field.id} label`).toBeTruthy();
        expect(field.type, `${section.id}.${field.id} type`).toBeTruthy();
      }
    }
  });

  it('field ids are unique within each section', () => {
    for (const section of CARE_PLAN_SECTIONS) {
      const ids = section.fields.map((f) => f.id);
      expect(new Set(ids).size, `duplicate field ids in ${section.id}`).toBe(ids.length);
    }
  });

  it('every field.type is one of FIELD_TYPES', () => {
    const validTypes = new Set(Object.values(FIELD_TYPES));
    for (const section of CARE_PLAN_SECTIONS) {
      for (const field of section.fields) {
        expect(validTypes.has(field.type), `bad type on ${section.id}.${field.id}: ${field.type}`).toBe(true);
      }
    }
  });

  it('select / multiselect / prn fields have non-empty options (or prn uses default)', () => {
    const typesRequiringOptions = [FIELD_TYPES.SELECT, FIELD_TYPES.MULTISELECT];
    for (const section of CARE_PLAN_SECTIONS) {
      for (const field of section.fields) {
        if (typesRequiringOptions.includes(field.type)) {
          expect(
            Array.isArray(field.options) && field.options.length > 0,
            `${section.id}.${field.id} missing options`,
          ).toBe(true);
        }
        if (field.type === FIELD_TYPES.PRN && field.options) {
          expect(field.options.length).toBeGreaterThan(0);
        }
      }
    }
  });

  it('list fields define subfields with valid types', () => {
    const validTypes = new Set(Object.values(FIELD_TYPES));
    for (const section of CARE_PLAN_SECTIONS) {
      for (const field of section.fields) {
        if (field.type === FIELD_TYPES.LIST) {
          expect(Array.isArray(field.subfields), `${section.id}.${field.id} missing subfields`).toBe(true);
          expect(field.subfields.length).toBeGreaterThan(0);
          for (const sub of field.subfields) {
            expect(sub.id).toBeTruthy();
            expect(sub.label).toBeTruthy();
            expect(validTypes.has(sub.type)).toBe(true);
          }
        }
      }
    }
  });

  it('conditional fields reference a sibling field that exists in the same section', () => {
    for (const section of CARE_PLAN_SECTIONS) {
      const fieldIds = new Set(section.fields.map((f) => f.id));
      for (const field of section.fields) {
        if (field.conditional) {
          expect(
            fieldIds.has(field.conditional.field),
            `${section.id}.${field.id} conditional references missing sibling '${field.conditional.field}'`,
          ).toBe(true);
        }
      }
    }
  });

  it('snapshot section has exactly one readonly field (the narrative)', () => {
    const snapshot = getSectionById('snapshot');
    expect(snapshot.fields).toHaveLength(1);
    expect(snapshot.fields[0].id).toBe('narrative');
    expect(snapshot.fields[0].readOnly).toBe(true);
  });

  it('tiers values are all from VISIBILITY_TIERS', () => {
    const validTiers = new Set(Object.values(VISIBILITY_TIERS));
    for (const section of CARE_PLAN_SECTIONS) {
      for (const tier of section.tiers) {
        expect(validTiers.has(tier), `${section.id} has invalid tier: ${tier}`).toBe(true);
      }
    }
  });
});

