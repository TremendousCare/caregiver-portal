describe('getSectionById', () => {
  it('returns the matching section', () => {
    expect(getSectionById('healthProfile')?.label).toBe('Health Profile');
  });

  it('returns undefined for an unknown id', () => {
    expect(getSectionById('totally-not-a-section')).toBeUndefined();
  });

  it('returns undefined for a missing id', () => {
    expect(getSectionById(null)).toBeUndefined();
    expect(getSectionById(undefined)).toBeUndefined();
    expect(getSectionById('')).toBeUndefined();
  });
});

describe('sortedSections', () => {
  it('returns sections ordered by .order ascending', () => {
    const list = sortedSections();
    for (let i = 1; i < list.length; i++) {
      expect(list[i].order).toBeGreaterThan(list[i - 1].order);
    }
  });

  it('does not mutate the underlying array', () => {
    const before = [...CARE_PLAN_SECTIONS];
    sortedSections();
    expect(CARE_PLAN_SECTIONS).toEqual(before);
  });

  it('puts snapshot first', () => {
    expect(sortedSections()[0].id).toBe('snapshot');
  });
});

describe('sectionUsesTasks', () => {
  it('returns true for dailyLiving', () => {
    expect(sectionUsesTasks(getSectionById('dailyLiving'))).toBe(true);
  });

  it('returns true for homeAndLife', () => {
    expect(sectionUsesTasks(getSectionById('homeAndLife'))).toBe(true);
  });

  it('returns false for non-task sections', () => {
    expect(sectionUsesTasks(getSectionById('whoTheyAre'))).toBe(false);
    expect(sectionUsesTasks(getSectionById('healthProfile'))).toBe(false);
    expect(sectionUsesTasks(getSectionById('dailyRhythm'))).toBe(false);
  });

  it('returns false for null / undefined', () => {
    expect(sectionUsesTasks(null)).toBe(false);
    expect(sectionUsesTasks(undefined)).toBe(false);
  });
});

describe('visibleSectionsForTier', () => {
  it('returns only admin-visible sections for admin tier', () => {
    const admin = visibleSectionsForTier('admin').map((s) => s.id);
    expect(admin).toContain('matchCriteria');
    expect(admin).toContain('healthProfile');
    expect(admin).toContain('snapshot');
  });

  it('caregiver tier excludes admin-only matchCriteria', () => {
    const caregiver = visibleSectionsForTier('caregiver').map((s) => s.id);
    expect(caregiver).not.toContain('matchCriteria');
    expect(caregiver).toContain('healthProfile');
  });

  it('family tier only includes sections tagged for family', () => {
    const family = visibleSectionsForTier('family').map((s) => s.id).sort();
    expect(family).toEqual(
      ['dailyLiving', 'dailyRhythm', 'homeAndLife', 'whoTheyAre'].sort(),
    );
  });

  it('returns empty array for unknown or missing tier', () => {
    expect(visibleSectionsForTier('nope')).toEqual([]);
    expect(visibleSectionsForTier(null)).toEqual([]);
    expect(visibleSectionsForTier(undefined)).toEqual([]);
  });

  it('results are sorted by order', () => {
    const list = visibleSectionsForTier('caregiver');
    for (let i = 1; i < list.length; i++) {
      expect(list[i].order).toBeGreaterThan(list[i - 1].order);
    }
  });
});

describe('getFieldById', () => {
  it('returns the matching field within a section', () => {
    const f = getFieldById('whoTheyAre', 'fullName');
    expect(f?.label).toBe('Full legal name');
  });

  it('returns undefined for an unknown field', () => {
    expect(getFieldById('whoTheyAre', 'nonsense')).toBeUndefined();
  });

  it('returns undefined for an unknown section', () => {
    expect(getFieldById('nonsense', 'fullName')).toBeUndefined();
  });

  it('returns undefined for missing args', () => {
    expect(getFieldById(null, 'fullName')).toBeUndefined();
    expect(getFieldById('whoTheyAre', null)).toBeUndefined();
    expect(getFieldById(null, null)).toBeUndefined();
  });
});

