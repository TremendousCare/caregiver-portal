describe('TASK_CATEGORIES', () => {
  it('every category maps to a section that exists', () => {
    for (const [category, meta] of Object.entries(TASK_CATEGORIES)) {
      expect(getSectionById(meta.section)).toBeDefined();
      expect(meta.label).toBeTruthy();
      expect(category).toMatch(/^(adl|iadl)\./);
    }
  });

  it('only routes to dailyLiving or homeAndLife sections', () => {
    const targets = new Set(Object.values(TASK_CATEGORIES).map((m) => m.section));
    expect(targets).toEqual(new Set(['dailyLiving', 'homeAndLife']));
  });

  it('adl.* categories route to dailyLiving', () => {
    for (const [category, meta] of Object.entries(TASK_CATEGORIES)) {
      if (category.startsWith('adl.')) {
        expect(meta.section).toBe('dailyLiving');
      }
    }
  });

  it('iadl.* categories route to homeAndLife', () => {
    for (const [category, meta] of Object.entries(TASK_CATEGORIES)) {
      if (category.startsWith('iadl.')) {
        expect(meta.section).toBe('homeAndLife');
      }
    }
  });
});

describe('sectionIdForCategory', () => {
  it('returns dailyLiving for adl.bathing', () => {
    expect(sectionIdForCategory('adl.bathing')).toBe('dailyLiving');
  });

  it('returns homeAndLife for iadl.housework', () => {
    expect(sectionIdForCategory('iadl.housework')).toBe('homeAndLife');
  });

  it('returns null for an unknown category', () => {
    expect(sectionIdForCategory('bogus.category')).toBeNull();
    expect(sectionIdForCategory('')).toBeNull();
    expect(sectionIdForCategory(null)).toBeNull();
  });
});
