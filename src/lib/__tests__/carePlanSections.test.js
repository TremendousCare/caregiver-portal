import { describe, it, expect } from 'vitest';
import {
  CARE_PLAN_SECTIONS,
  FIELD_TYPES,
  VISIBILITY_TIERS,
  TASK_CATEGORIES,
  getSectionById,
  sectionIdForCategory,
  sectionUsesTasks,
  sortedSections,
  visibleSectionsForTier,
  getFieldById,
} from '../../features/care-plans/sections';

// ═══════════════════════════════════════════════════════════════
// Section catalog
// ═══════════════════════════════════════════════════════════════

describe('CARE_PLAN_SECTIONS', () => {
  it('defines 11 sections (Phase 2b taxonomy)', () => {
    expect(CARE_PLAN_SECTIONS).toHaveLength(11);
  });

  it('every section has id, label, description, order, tiers', () => {
    for (const section of CARE_PLAN_SECTIONS) {
      expect(section.id).toBeTruthy();
      expect(section.label).toBeTruthy();
      expect(section.description).toBeTruthy();
      expect(typeof section.order).toBe('number');
      expect(Array.isArray(section.tiers)).toBe(true);
      expect(section.tiers.length).toBeGreaterThan(0);
    }
  });

  it('section ids are unique', () => {
    const ids = CARE_PLAN_SECTIONS.map((s) => s.id);
    expect(new Set(ids).size).toBe(ids.length);
  });

  it('section order values form a contiguous 0..N range', () => {
    const orders = CARE_PLAN_SECTIONS.map((s) => s.order).sort((a, b) => a - b);
    for (let i = 0; i < orders.length; i++) {
      expect(orders[i]).toBe(i);
    }
  });

  it('snapshot is at order 0 and is flagged as AI-generated', () => {
    const snapshot = CARE_PLAN_SECTIONS.find((s) => s.id === 'snapshot');
    expect(snapshot).toBeDefined();
    expect(snapshot.order).toBe(0);
    expect(snapshot.isAutoGenerated).toBe(true);
  });

  it('dailyLiving and homeAndLife are the only task-table sections', () => {
    const taskSections = CARE_PLAN_SECTIONS.filter((s) => s.usesTasksTable);
    expect(taskSections.map((s) => s.id).sort()).toEqual(['dailyLiving', 'homeAndLife']);
  });

  it('matchCriteria is admin-only (no caregiver or family visibility)', () => {
    const match = getSectionById('matchCriteria');
    expect(match?.tiers).toEqual(['admin']);
  });

  it('expected family-visible sections are tagged for family tier', () => {
    const familySections = CARE_PLAN_SECTIONS
      .filter((s) => s.tiers.includes('family'))
      .map((s) => s.id)
      .sort();
    expect(familySections).toEqual(
      ['dailyLiving', 'dailyRhythm', 'homeAndLife', 'whoTheyAre'].sort(),
    );
  });
});

